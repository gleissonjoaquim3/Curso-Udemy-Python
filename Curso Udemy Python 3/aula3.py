def saudacao(msg='Ola', nome='usuaruo'):
    nome = nome.replace('e','3')
    msg = msg.replace('f','2')
    print(msg, nome)

saudacao(nome='Zezinho', msg='FalaHi')
saudacao('Hello', 'Ok')
saudacao('teste', 'tester')
saudacao('res', 'redfd')


